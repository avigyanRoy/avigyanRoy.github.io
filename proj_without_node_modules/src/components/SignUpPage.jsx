import React,{useState} from "react";
import axios from "axios";
import {useNavigate} from "react-router-dom";


function SignUpPage() {


    const [name, setName]= useState("");
    const [email, setEmail]= useState("");
    const [password, setPassword]= useState("");
    const navigate = useNavigate();
    async function submit(e){
        e.preventDefault();

        try{
             axios.post("http://localhost:3001/signup",{
                name,email,password
            })
            .then(res=>{
                if(res.data==="exists"){
                    alert("Account already exists");
                    navigate("/login",{state: {id:email}});
                }
                else if(res.data==="doesn't exist"){
                    alert("Account created successfully!");

                    navigate("/login",{state: {id:email}});
                } 
            })
            .catch(e=>{
                alert("wrong details entered");
                console.log(e);
            });
        }
        catch(e){
            console.log(e);
        }
    }

    return (
        <div className="sign-up-container">
            <div className="cta-container">
                <div className="cta-heading">Join the Stack Overflow community</div>
                <div className="cta-features">
                    <div className="cta-feature-container">
                        <img src="https://www.seekpng.com/png/detail/24-244080_ask-a-question-ask-a-question-icon.png" alt="" className="cta-feature-image" />
                        <div className="cta-feature-content">Get unstuck — ask a question</div>
                    </div>
                    <div className="cta-feature-container">
                        <img src="https://cdn-icons-png.flaticon.com/512/37/37808.png" alt="" className="cta-feature-image" />
                        <div className="cta-feature-content">Unlock new privileges like voting and commenting</div>
                    </div>
                    <div className="cta-feature-container">
                        <img src="https://static.vecteezy.com/system/resources/thumbnails/005/200/965/small/bookmark-black-color-icon-vector.jpg" alt="" className="cta-feature-image" />
                        <div className="cta-feature-content">Save your favorite questions, answers, watch tags, and more</div>
                    </div>
                    <div className="cta-feature-container">
                        <img src="https://cdn3.vectorstock.com/i/1000x1000/77/97/gold-medal-reputation-icon-simple-style-vector-33947797.jpg" alt="" className="cta-feature-image" />
                        <div className="cta-feature-content">Earn reputation and badges</div>
                    </div>
                </div>
            </div>
            <div className="registration-container">
                <div className="registration-card">
                    <div className="input-fields-container">
                        <div className="input-title">Display Name</div>
                        <input  type="text" onChange={(e)=>{setName(e.target.value)}}/>
                    </div>
                    <div className="input-fields-container">
                        <div className="input-title">Email</div>
                        <input  type="email"  onChange={(e)=>{setEmail(e.target.value)}}/>
                    </div>
                    <div className="input-fields-container">
                        <div className="input-title">Password</div>
                        <input   type="text" onChange={(e)=>{setPassword(e.target.value)}}/>
                        <div className="password-condition">Passwords must contain at least eight characters, including at least 1 letter and 1 number.</div>
                    </div>
                    <button className="sign-up-button" onClick={submit}>Sign up</button>
                    <div className="terms-and-conditions">By clicking “Sign up”, you agree to our <a href="#">terms of service</a> and acknowledge that you have read and understand our <a href="#">privacy policy</a> and code of conduct.</div>
                </div>
                <div className="log-in-if-account-present">Already have an account? <a href="/login">Log in</a> </div>
            </div>
        </div>
    );
}

export default SignUpPage;