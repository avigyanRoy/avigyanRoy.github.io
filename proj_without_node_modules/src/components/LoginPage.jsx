import React,{useState} from "react";
import axios from "axios";
import { useNavigate} from "react-router-dom";
function LoginPage(){
    
    const [email, setEmail]= useState("");
    const [password, setPassword]= useState("");
    const navigate = useNavigate();
    async function submit(e){
        e.preventDefault();

        try{
            axios.post("http://localhost:3001/login",{
                email,password
            })
            .then(res=>{
                if(res.data==="exists"){
                    navigate("/home",{state: {id:email}});
                    console.log("Login Success");
                }
                else if(res.data==="doesn't exist"){
                    alert("No account found.");
                } 
                else if(res.data==="Incorrect password"){
                    alert(res.data);
                }
                
            })
            .catch(e=>{
                alert("wrong details entered");
                console.log(e);
            })
        }
        catch(e){
            console.log(e);
        }
    }
    return (
        <div className="login-container">
                <img className="logo-login" src="https://upload.wikimedia.org/wikipedia/commons/thumb/e/ef/Stack_Overflow_icon.svg/768px-Stack_Overflow_icon.svg.png" alt="" />
                <div className="login-card">
                    
                    <div className=" login-fields-container">
                        <div className=" login-input-title">Email</div>
                        <input type="email" onChange={(e)=>{setEmail(e.target.value)}} />
                   
                    
                        <div className=" login-input-title">Password </div>
                        <input type="password" onChange={(e)=>{setPassword(e.target.value)}} />
                        
                    </div>
                    <button className="sign-up-button sign-in-button" onClick={submit}>Log in</button>
                    {/* <div className="forgot-password"><a href="#">Forgot password?</a></div> */}
                </div>
                <div className="log-in-if-account-present sign-in-if-no-acc">Don't have an account? <a href="/signup">Sign up</a> </div>
            
        </div>
    );
}

export default LoginPage;