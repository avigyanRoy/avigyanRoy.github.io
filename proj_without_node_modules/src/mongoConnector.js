const mongoose = require("mongoose");

const connectDatabase = async () => {
  const username = encodeURIComponent("avigyan_me");
  const password = encodeURIComponent("Paramita1@");
  const clusterUrl = "cluster0.gtbzihy.mongodb.net";
  const databaseName = "user-db";

  const connectionString = `mongodb+srv://${username}:${password}@${clusterUrl}/${databaseName}`;

  await mongoose
    .connect(connectionString, {
      useNewUrlParser: true,
      useUnifiedTopology: true 
    })
      .then(() => {
        console.log('MongoDB connected');
      })
      .catch((error) => {
        console.log('Failed to connect to MongoDB:', error);
      });
};

module.exports = connectDatabase;