const mongoose= require("mongoose");




const qa_Schema= new mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    author_name: {
        type:String,
        required: false
    },
    up_votes:{
        type: Number,
        required: true
    },
    down_votes:{
        type: Number,
        required: true
    },
    no_of_answers:{
        type: Number,
        required: true
    },
    description:{
        type: String,
        required: false
    },
    answers:{
        type: [String],
        required: false
    },
    answer_up_votes:{
        type: [Number],
        required: false
    },
    answer_down_votes:{
        type: [Number],
        required: false
    }


});

module.exports = mongoose.model("qa_collection", qa_Schema);

